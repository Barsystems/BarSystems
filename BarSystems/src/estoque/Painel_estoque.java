    private void btnNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoActionPerformed
        
        String centro = null;
        centro = JOptionPane.showInputDialog(null, "Digite o nome do centro de estoque", "Atenção", JOptionPane.PLAIN_MESSAGE);
        if (centro == null || centro.equals("")) {
        } else {
            Class_estoque estoque = new Class_estoque();
            if (estoque.verificaNomeRepetidoCentro(centro) == true) {
                JOptionPane.showMessageDialog(null, "Este centro de estoque já existe!", "Atenção", JOptionPane.WARNING_MESSAGE);
            } else {
                estoque.cadastraCentro(centro);
                lista_centros.setModel(estoque.carregaLista());
            }
        }
        
    }//GEN-LAST:event_btnNovoActionPerformed


