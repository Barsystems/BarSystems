    private void btnTransferirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTransferirActionPerformed
        
        int linha = tabela_estoque.getSelectedRow();
        if (linha == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um produto na tabela ao lado!", "Atenção", JOptionPane.WARNING_MESSAGE);
        } else {
            DefaultTableModel modelo = (DefaultTableModel) tabela_estoque.getModel();
            produto.setText(modelo.getValueAt(linha, 0).toString());
            centro_estoque.setText(lista_centros.getSelectedValue().toString());
            quant_unidade.setText(modelo.getValueAt(linha, 1).toString());
            
            refreshTransf(lista_centros.getSelectedValue().toString());
            
            subir_produto.setBounds(0, 0, 450, 430);
            subir_produto.setLocationRelativeTo(null);
            subir_produto.setVisible(true);
            
        }
        
    }//GEN-LAST:event_btnTransferirActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       
       Class_estoque estoque  = new Class_estoque();
       if(quantidade.getText().equals("")){
           JOptionPane.showMessageDialog(null, "Digite uma quantidade");
       } else if(lista_centro2.isSelectionEmpty()){
           JOptionPane.showMessageDialog(null, "Selecione um centro de estoque para enviar");
       } else {
            if (!(Integer.parseInt(quantidade.getText()) > Integer.parseInt(quant_unidade.getText()))){
                estoque.transferencia(lista_centro2.getSelectedValue().toString(), produto.getText(), 
                    Integer.parseInt(quantidade.getText()), centro_estoque.getText());
            } else {
                JOptionPane.showMessageDialog(null, "Quantidade maior que o estoque!", "Atenção", JOptionPane.WARNING_MESSAGE);
            }
       }

       int codigo = estoque.getCodigo((String) lista_centros.getSelectedValue());
       estoque.carregaTabela(codigo, (DefaultTableModel) tabela_estoque.getModel());
       
    }//GEN-LAST:event_jButton2ActionPerformed

