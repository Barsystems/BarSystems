    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed

        Object funcionario = listFuncionarios.getSelectedValue();
        if (funcionario == null) {
            JOptionPane.showMessageDialog(null, "Selecione um funcionário para excluir", "Atenção", JOptionPane.WARNING_MESSAGE);
        } else {
            if (JOptionPane.showConfirmDialog(null, "Deseja realmente excluir?", "Atenção", JOptionPane.YES_NO_OPTION) == 0) {
                int id_funcionario = Integer.valueOf(arrayList.get(listFuncionarios.getSelectedIndex()).toString());

                Class_Funcionarios funcionarios = new Class_Funcionarios();
                funcionarios.excluirFuncionario(id_funcionario);

                refreshFuncionarios();
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

