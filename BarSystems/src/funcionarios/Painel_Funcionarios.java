    private void btnCadastrarFuncaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarFuncaoActionPerformed

        String funcao = null;
        funcao = JOptionPane.showInputDialog(null, "Digite o nome da função a ser cadastrada", "Atenção", JOptionPane.PLAIN_MESSAGE);
        if (funcao == null || funcao.equals("")) {
        } else {
            Class_Funcionarios funcionarios = new Class_Funcionarios();
            if (funcionarios.verificaFuncaoRepetida(funcao) == true) {
                JOptionPane.showMessageDialog(null, "Esta função já existe!", "Atenção", JOptionPane.WARNING_MESSAGE);
            } else {
                funcionarios.cadastrarFuncao(funcao);
                listFuncoes.setModel(funcionarios.refreshFuncoes());
            }
        }

    }//GEN-LAST:event_btnCadastrarFuncaoActionPerformed

    private void btnEditarFuncaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarFuncaoActionPerformed

        Object funcao = listFuncoes.getSelectedValue();
        if (funcao == null) {
            JOptionPane.showMessageDialog(null, "Selecione uma função para editar!", "Atenção", JOptionPane.WARNING_MESSAGE);
        } else {
            String nova_funcao = null;
            nova_funcao = JOptionPane.showInputDialog(null, "Digite o novo nome da função", "Atenção", JOptionPane.PLAIN_MESSAGE);
            if (nova_funcao == null || nova_funcao.equals("")) {
            } else {
                Class_Funcionarios funcionarios = new Class_Funcionarios();
                if (funcionarios.verificaFuncaoRepetida(nova_funcao) == true && !nova_funcao.equals(funcao.toString())) {
                    JOptionPane.showMessageDialog(null, "Esta função já existe!", "Atenção", JOptionPane.WARNING_MESSAGE);
                } else {
                    funcionarios.alterarFuncao(funcao.toString(), nova_funcao);
                    listFuncoes.setModel(funcionarios.refreshFuncoes());
                }
            }
        }

    }//GEN-LAST:event_btnEditarFuncaoActionPerformed

    private void btnExcluirFuncaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirFuncaoActionPerformed

        Object funcao = listFuncoes.getSelectedValue();
        if (funcao == null) {
            JOptionPane.showMessageDialog(null, "Selecione uma função para excluir!", "Atenção", JOptionPane.WARNING_MESSAGE);
        } else {
            Class_Funcionarios funcionarios = new Class_Funcionarios();
            if (funcionarios.verificaFuncaoPadrao(funcao.toString()) == true) {
                JOptionPane.showMessageDialog(null, "Esta é uma função padrão e não poderá ser excluída!", "Atenção", JOptionPane.WARNING_MESSAGE);
            } else if (funcionarios.verificaFuncaoVinculadaAoFuncionario(funcionarios.retornaIdFuncao(funcao.toString())) == true) {
                JOptionPane.showMessageDialog(null, "Esta função está vinculada a um funcionário e não poderá ser excluída!", "Atenção", JOptionPane.WARNING_MESSAGE);
            } else if (JOptionPane.showConfirmDialog(null, "Deseja realmente excluir?", "Atenção", JOptionPane.YES_NO_OPTION) == 0) {
                funcionarios.excluirFuncao(funcao.toString());
            }
        }

    }//GEN-LAST:event_btnExcluirFuncaoActionPerformed

